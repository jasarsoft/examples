
public class Koren {
	
	public static void main(String[] args) {
        int i = 2;
        double r = Math.sqrt(i);
        
        System.out.print("Kvadratni koren od ");
        System.out.print(i);
        System.out.print(" je ");
        System.out.print(r);
        System.out.println(".");

        i = 5;
        r = Math.sqrt(i);
        System.out.println("Kvadratni koren od " + i + " je " + r + ".");       
    }

}
