
package jasarsoft;


public class Primjer08 {

    public static void main(String[] args) {
        
        int x = 777;
        
        ++x;
        ++x;
        
        --x;
        
        System.out.println(x);
        
        
        x = 1;
        System.out.println(x++);
        System.out.println(x);
        
        x = 1;
        System.out.println(++x);
        System.out.println(x);
    }
    
}
